#include <stdio.h>

/**
 * op_add - A function that adds two int
 * @a: An input
 * @b: An input
 * Return: returns the added result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub- A function that perform substration on two int
 * @a: an input
 * @b: an input
 * Return: returns the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul- A function thatmultiply on two int
 * @a: an input
 * @b: an input
 * Return: returns the result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - A function that divides two int
 * @a: an input
 * @b: an input
 * Return: returns the result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - A function that divides two int and return the remainder
 * @a: an input
 * @b: an input
 * Return: returns the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
